<center>
    <h1>Componente 2:</h1>
    <h1>Fortalecimiento de Gobiernos Locales</h1>
</center>

<p>Este componente consiste en fortalecer las capaciodades y el rol de los Gobiernos
Locales en los procesos de desarrollo local. Sus Objetivos son:
</p>
<ul>
    <li>Proveer asistencia técnica y capacitaación a los municipios para desarrollar y fortalecer
    sus capacidades de gestión administrativa, procesos de adquiciones, gestión financiera,
    manejo de recursos humanos y planificación participativa; asi como prepararles y fortalecerles
    para la ejecución de los subproyectos en Componente 1</li>
    <li>Asistir en la impkementación de la Carrera Administratica Municipal.</li>
    <Li>Fortalecer procesos y sistemas para incrementar la capacidad de los Gobiernos Locales (GL) en el
    planeamiento participativo para el desarrollo</li>
    <Li>Fortalecer sistemas administrativos procesos de contrataciones y adquiciones, gestión
    financiera tranparente de los GL (acceso a información pública, un sistema de monitoreo y 
    rendición de cuentas)</li>
    
</ul>
<p>Este componente se implementará a través de los subcompoenentes siguientes:</p>
<ol>
    <li>Subcomponente 2.1: Apoyo a inversiones municipales</li>
    <li>Subcomponente 2.2: Apoyo a la profeesionalizaciòn de la Carrera Administrativa
    Municipal</li>
    <li>Subcomponente 2.3: Procesos de Planificación Participativa y Concertada</li>
    <li>Subcomponente 2.4: Administración Financiera y de adquisiciones</li>
    <li>Subcomponente 2.5: Fortalecimiento institucional local para la gestión de riesgos</li>
    <li>Subcomponente 2.6: Fortalecimiento de instituciones de apoyo al desarrollo local y la 
    descentralización</li>
</ol>