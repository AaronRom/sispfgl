<center>
    <h1>SubComponente 2.3.</h1>
    <h1>Procesos de Planificación Participativa y Concertada</h1>
</center>

<p>Consiste en presentar asistencia tècnica a 114 Gobiernos Locales, seleccionados a partir de 
    determinados criterios y de forma gradual, en la promoción y ejecución de procesos participativos 
    de desarrollo local, mejorando metodologías de planificación estratégica o de desarrollo municipal
    con énfasis en desarrollo económico local y una visión de mediana y largo plazo. El proceso de planificación 
    permitirá a los Gobiernos Locales, junto a su población y otros actores locales, identificar
    y priorizar los problemas y soluciones de sus comunidades y municipios, así como desarrollar
    ejercicios de concertación entre los diferentes actores del territorio y el fortalecimiento de mecanismos
    de participación ciudadana que den sostenibilidad a dichos procesos, así como la elaboración
    de guñias que faciliten la asistencia técnica y contribuyan a la participación de los ciudadanos y ciudadanas.
</p>